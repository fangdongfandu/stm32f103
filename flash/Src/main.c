static void sendstring(char *str)
{
    while(*str!='\0')
    {
        USART1->DR = (*str & 0x1FF);
        while(!(USART1->SR & UART_FLAG_TXE));
        str++;
    }
}

void log_printf(char* format,...)
{
    va_list ap;
    char len = 0;
    char print_level = '0';
    char *p = format;

    if (p[0] == '<' && p[1] >= '0' && p[1] <= '9' && p[2] == '>') {
        len  = 3;
        print_level = p[1];
    }
    if(debug_log_on + '0' >= print_level)
    {
        va_start(ap, format);
        vsprintf(buffer, format,ap);
        sendstring(buffer + len);
        va_end(ap);
    }

}
